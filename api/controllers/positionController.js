const PositionsModels = require('../Models/positionModels')

const GET = {
	async getAllPositions(req, reply) {
		try {
			const result = await PositionsModels.getAllPositions()
			if (!result.success) {
				console.error('Ошибка базы данных: ', result.message)
				return reply
					.status(502)
					.send({ success: false, message: result.message })
			}
			reply.send({ success: true, data: result.data })
		} catch (error) {
			console.log(error)
			reply.status(500).send({ success: false, message: error })
		}
	},
	async getPositionById(req, reply) {
		try {
			const { id } = req.params
			const result = await PositionsModels.getPositionById(id)

			if (!result.success) {
				console.error('Ошибка базы данных: ', result.message)
				return reply
					.status(500)
					.send({ success: false, message: result.message })
			}
			if (result.data.length <= 0) {
				return reply
					.status(500)
					.send({ success: false, message: 'Должность не найдена' })
			}
			reply.send({ success: true, data: result.data[0] })
		} catch (error) {
			reply.status(500).send({ success: false, message: error })
		}
	},
}

const POST = {
	async createPosition(req, reply) {
		try {
			const { title, salary } = req.body
			if (!title)
				return reply.status(500).send({ success: false, message: 'нет title' })
			if (!salary)
				return reply.status(500).send({ success: false, message: 'нет salary' })
			const result = await PositionsModels.createPosition(title, salary)
			if (!result.success) {
				console.error('Ошибка базы данных: ', result.message)
				return reply
					.status(502)
					.send({ success: false, message: result.message })
			}
			reply.send({ success: true, data: result.data[0] })
		} catch (error) {
			console.log(error)
			reply.status(500).send({ success: false, message: error })
		}
	},
}

const PUT = {
	async updatePosition(req, reply) {
		try {
			const { id } = req.params
			const { title, salary } = req.body
			const result = await PositionsModels.updatePosition(id, title, salary)
			if (!result.success) {
				console.error('Ошибка базы данных: ', result.message)
				return reply
					.status(502)
					.send({ success: false, message: result.message })
			}
			reply.send({ success: true, data: result.data[0] })
		} catch (error) {
			console.log(error)
			reply.status(500).send({ success: false, message: error })
		}
	},
}

const DELETE = {
	async deletePosition(req, reply) {
		try {
			const { id } = req.params
			const result = await PositionsModels.deletePosition(id)
			if (!result.success) {
				console.error('Ошибка базы данных: ', result.message)
				return reply
					.status(502)
					.send({ success: false, message: result.message })
			}
			reply.send({ success: true, data: { id: id } })
		} catch (error) {
			console.log(error)
			reply.status(500).send({ success: false, message: error })
		}
	},
}

module.exports = {
	GET,
	POST,
	PUT,
	DELETE,
}
